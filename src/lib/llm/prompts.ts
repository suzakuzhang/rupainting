// 模式 3：画中漫游（Roam Mode）
const MODE_ROAM = `
当前模式：画中漫游（Roam Mode）

适合：体验性观看，用户想"走进画面"
风格：第二人称引导，有空间感，但不是冒险游戏，是审美性视线训练。
长度：~500-800 字。

严格底线：
- 漫游不是占卜。结尾必须把用户视线引回画面具体元素，绝不引向用户人生命运
- 用"假设你的视线进入……"而不是"你将看到……"这种预言式句法
- 每段必须落在画面证据上

入口由用户输入或随机选择（如"从舟进入"、"从题跋进入"、"从一只孤鸟进入"）。

输出格式（合法 JSON）：
{
  "entry_point": "你选择的入口在画面什么位置，眼睛先看到什么。100-150 字。",
  "walk_through": "沿这条入口往画面深处走，依次会看见什么、感受到什么、画面的墨色和留白如何展开。300-450 字。",
  "visual_anchor_back": "停下来——把视线收回画面的某个具体细节（一片墨色、一方印章、一笔留白），让漫游有一个回到画面的锚点。100-150 字。"
}
`.trim();

// 模式 4：研究笔记（Research Notes）
const MODE_NOTES = `
当前模式：研究笔记（Research Notes）

适合：学术用例，用户想得到可保存的 Markdown
输出格式严格固定 — 把内容输出成一个完整的 Markdown 字符串，包在 JSON 里：

{
  "markdown": "## 作品信息\\n（画家、年代、款署、材质、尺寸、收藏地、入藏年/捐赠人）\\n\\n## 可见元素\\n（主体 / 次要 / 装饰 / 题跋位置 / 印章位置）\\n\\n## 构图结构\\n（视点 / 景别 / 开合 / 留白 / 视觉引导）\\n\\n## 笔墨语言\\n（线条 / 皴法 / 墨色层次 / 设色 / 笔速节奏）\\n\\n## 题跋与印章\\n（按时间顺序列出可识读的题跋者与钤印者）\\n\\n## 历史语境\\n（画家身份、所处时代、是否易代期/被贬期、与同时代作品关系）\\n\\n## 可能的问题意识\\n（在艺术史/思想史中可被追问的问题）\\n\\n## 可引用的观察\\n（可直接进入论文/作品集的句子，短引）\\n\\n## 待查证信息\\n（真伪争议 / 著录归属 / 同名异本 / 题跋断代等）"
}

模板里的每个章节都必须填写实际内容。如果某项无法判断（如题跋未识读），写"待考"或"画面未提供"。
不要自由发挥跳出模板。所有正文用 Markdown，不要嵌套 JSON。
`.trim();

function modeBlock(mode: ReadingMode): string {
  switch (mode) {
    case "beginner": return MODE_BEGINNER;
    case "scholar": return MODE_SCHOLAR;
    case "roam": return MODE_ROAM;
    case "notes": return MODE_NOTES;
  }
}

// ────────────────────────────────────────────────────────────────────────────
// 系统提示词与用户提示词组装
// ────────────────────────────────────────────────────────────────────────────

export function buildInterpretSystemPrompt(mode: ReadingMode): string {
  return [
    METHODOLOGY_CORE,
    "",
    READING_BOUNDARIES,
    "",
    KEY_TERMS,
    "",
    HEARN_LENSES,
    "",
    SEVEN_LAYER_DEFINITION,
    "",
    modeBlock(mode),
  ].join("\n\n");
}

export function buildInterpretUserPrompt(
  painting: Painting,
  artist: Artist,
  mode: ReadingMode,
  question?: string,
  roamEntry?: string
): string {
  const lensesLine = painting.reading_lenses.length
    ? `推荐读画镜头：${painting.reading_lenses.join(" / ")}`
    : "推荐读画镜头：（未指定）";

  const modeSpecificLine =
    mode === "roam"
      ? `\n用户选择的漫游入口：${roamEntry || "（未指定，请你选择一个画面证据明确的入口，并在 entry_point 中说明为什么从这里进入）"}`
      : question
      ? `\n用户带着的具体问题：${question}\n（请把这个问题贯穿到解读中，但不要变成回答问题的口吻——它应该融进你对画面的观察里。）`
      : "\n用户没有特定问题，请按方法论默认展开。";

  return `请按以下作品信息进行读画，输出合法 JSON：

【作品信息】
标题：${painting.title}${painting.alt_titles?.length ? `（别题：${painting.alt_titles.join("、")}）` : ""}
${painting.series ? `所属：${painting.series}${painting.series_part ? `（${painting.series_part}）` : ""}` : ""}
画家：${artist.name}（${artist.given_name}，${artist.dates}）
原籍：${artist.origin}
材质：${painting.medium}
尺寸：${painting.dimensions}
形制：${painting.format}
收藏：${painting.collection}
约定年代：${painting.approximate_date ?? "待考"}${painting.dated ? "（画上有明确纪年）" : ""}

【画家背景】
${artist.background}
关键母题：${artist.key_motifs.join("、")}
关键概念：${artist.key_concepts.join(" / ")}

【画面证据 — 这是你必须落到的锚点】
可见元素：${painting.visible_elements}
构图：${painting.composition_notes}
笔墨：${painting.brushwork_notes}
题跋：${painting.inscription || "无明显题跋"}
印章：${painting.seals_visible || "无明显印章"}
情绪场（参考词）：${painting.emotional_field.join("、")}
${painting.research_notes ? `研究备注：${painting.research_notes}` : ""}

${lensesLine}
${modeSpecificLine}

输出要求：
1. 严格按系统提示中当前模式的 JSON 格式输出
2. 所有论断必须落到上面"画面证据"或"画家背景"里能查到的事实
3. 不要把作品标题/画家名作为唯一证据——主要证据来自画面元素
4. 引用题跋/印章原文时用「」标注
5. 输出合法 JSON，不输出 JSON 以外的内容`;
}

