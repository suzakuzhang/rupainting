const SEVEN_LAYER_DEFINITION = `
七层观看结构（每次解读的固定骨架）：

1. 第一眼看见什么 — 不急于解释。直接描述画面最直接的可见元素：主体、位置、墨色范围、人物/动物/植物/建筑。
2. 观看路线 — 中国画几乎从不要求一眼看完。指出眼睛该如何进入与移动：手卷从右起，立轴从下往上，册页有内置节奏；指出视线的引导线（水路、桥、人物目光、留白、远山层次）。
3. 笔墨与空间 — 线条（勾/皴/点/染）、笔速（干湿）、留白（云/水/雾/虚空）、构图（三远/开合/虚实）、墨色 vs 设色。
4. 题跋、印章与观看史 — 画家自题、同代/后世题、印章序列。它们不是污损画面，是这幅画为什么是杰作的证明。
5. 象征与文化语境 — 母题可能含义（避免一对一符号字典化），画家身份与时代，诗文/史料引述。
6. 这幅画的情绪 — 注意：是画作制造的情绪场，不是用户的情绪。区分情绪来自题材、笔墨节奏、还是留白比例。
7. 你可以继续问 — 给 3 个有具体关联的追问入口，每个追问是一个新镜头。
`.trim();

